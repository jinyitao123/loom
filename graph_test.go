func TestGraph_StepError_AbortsGraph(t *testing.T) {
	g := loom.NewGraph("test", "a")
	g.AddStep("a", echoStep("ok", "1"), loom.Always("b"))
	g.AddStep("b", failStep("something broke"), loom.Always("c"))
	g.AddStep("c", echoStep("unreachable", "true"), loom.End())

	result, err := g.Run(context.Background(), loom.State{}, nil)
	assertError(t, err, "something broke")
	assertState(t, result, "__error", "something broke")
	assertState(t, result, "__failed_step", "b")
	if result.State["unreachable"] != nil {
		t.Error("step c should not have executed")
	}
}

