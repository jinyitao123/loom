func TestGraph_Checkpoint_Saved(t *testing.T) {
	store := loom.NewMemStore()
	g := loom.NewGraph("myagent", "s")
	g.AddStep("s", echoStep("x", "1"), loom.End())

	result, _ := g.Run(context.Background(), loom.State{}, store)

	data, err := store.Get(context.Background(), "checkpoint:myagent", result.RunID)
	assertNoError(t, err)
	if len(data) == 0 {
		t.Error("checkpoint data is empty")
	}
}

