func TestGraph_SingleStep(t *testing.T) {
	g := loom.NewGraph("test", "only_step")
	g.AddStep("only_step", echoStep("output", "hello"), loom.End())

	result, err := g.Run(context.Background(), loom.State{}, nil)
	assertNoError(t, err)
	assertState(t, result, "output", "hello")
	assertSteps(t, result, 1)
}

func TestGraph_LinearChain(t *testing.T) {
	cfg := loom.DefaultMergeConfig()
	cfg.Register("trace", loom.AppendSlice)
	g := loom.NewGraph("test", "a", loom.WithMergeConfig(cfg))
	g.AddStep("a", collectStep("a"), loom.Always("b"))
	g.AddStep("b", collectStep("b"), loom.Always("c"))
	g.AddStep("c", collectStep("c"), loom.End())

	result, err := g.Run(context.Background(), loom.State{}, nil)
	assertNoError(t, err)
	assertTrace(t, result, "a", "b", "c")
	assertSteps(t, result, 3)
}

func TestGraph_ConditionalBranching(t *testing.T) {
	g := loom.NewGraph("test", "check")
	g.AddStep("check", echoStep("status", "pass"), loom.Branch("status", map[string]string{
		"pass": "success",
		"fail": "error",
	}, "error"))
	g.AddStep("success", echoStep("result", "ok"), loom.End())
	g.AddStep("error", echoStep("result", "failed"), loom.End())

	result, err := g.Run(context.Background(), loom.State{}, nil)
	assertNoError(t, err)
	assertState(t, result, "result", "ok")
	assertLastStep(t, result, "success")
}

func TestGraph_Loop_WithExit(t *testing.T) {
	g := loom.NewGraph("test", "inc")
	g.AddStep("inc", counterStep("n"), loom.Condition(
		func(s loom.State) bool { n, _ := s["n"].(int); return n >= 3 },
		"",    // halt when n >= 3
		"inc", // loop back
	))

	result, err := g.Run(context.Background(), loom.State{"n": 0}, nil)
	assertNoError(t, err)
	assertState(t, result, "n", 3)
	assertSteps(t, result, 3)
}

