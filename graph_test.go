func TestGraph_ContextCancellation(t *testing.T) {
	ctx, cancel := context.WithCancel(context.Background())
	cancel()

	g := loom.NewGraph("test", "s")
	g.AddStep("s", func(ctx context.Context, _ loom.State) (loom.State, error) {
		select {
		case <-ctx.Done():
			return nil, ctx.Err()
		default:
			return loom.State{"x": "1"}, nil
		}
	}, loom.End())

	_, err := g.Run(ctx, loom.State{}, nil)
	if err == nil {
		t.Error("expected context cancellation error")
	}
}

func TestGraph_StepBudget_ExhaustedAcrossSubgraphs(t *testing.T) {
	store := loom.NewMemStore()

	child := loom.NewGraph("child", "loop")
	child.AddStep("loop", counterStep("n"), loom.Always("loop"))

	parent := loom.NewGraph("parent", "run_child",
		loom.WithStepBudget(10),
		loom.WithMaxIterations(100),
	)
	parent.AddStep("run_child", func(ctx context.Context, state loom.State) (loom.State, error) {
		result, err := child.Run(ctx, state, store)
		if err != nil {
			return nil, err
		}
		return result.State, nil
	}, loom.End())

	_, err := parent.Run(context.Background(), loom.State{}, store)
	assertError(t, err, "step budget exhausted")
}

func TestGraph_MergeConfig_UsedInRun(t *testing.T) {
	cfg := loom.NewMergeConfig()
	cfg.Register("total", loom.SumInt)

	g := loom.NewGraph("test", "a", loom.WithMergeConfig(cfg))
	g.AddStep("a", func(_ context.Context, _ loom.State) (loom.State, error) {
		return loom.State{"total": 10}, nil
	}, loom.Always("b"))
	g.AddStep("b", func(_ context.Context, _ loom.State) (loom.State, error) {
		return loom.State{"total": 20}, nil
	}, loom.End())

	result, _ := g.Run(context.Background(), loom.State{"total": 5}, nil)
	assertState(t, result, "total", 35)
}
